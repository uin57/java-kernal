package com.hs3.models.lotts;

public class SimpleSeason {
    private String season;
    private String nums;

    public String getSeason() {
        return this.season;
    }

    public void setSeason(String season) {
        this.season = season;
    }

    public String getNums() {
        return this.nums;
    }

    public void setNums(String nums) {
        this.nums = nums;
    }
}
